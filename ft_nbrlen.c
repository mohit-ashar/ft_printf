#include "ft_printf.h"

int		ft_nbrlen(int n)
{
	int p;

	p = 0;
	if (n <= 0)
	{
		p = 1;
		n = -1 * n;
	}
	while (n > 0)
	{
		n = n / 10;
		p++;
	}
	return (p);
}
